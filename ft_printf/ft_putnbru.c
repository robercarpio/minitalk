#include "ft_printf.h"

int	ft_putnbru(unsigned int n)
{
	char	nb;
	int		i;

	i = 0;
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
	}
	nb = n % 10 + 48;
	i += ft_putchr(nb);
	return (i);
}
